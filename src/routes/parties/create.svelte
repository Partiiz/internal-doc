<h2>Inviter</h2>
<p>
	Choisir d'inscrire d'avance des amis que l'on a sur l'application | inviter des amis via facebook
	/ réseaux sociaux / un lien d'invitation, qui les redirigera vers l'appli pour les inscrire s'ils
	ne sont pas inscrits, qui les connctera s'ils sont déjà inscrits, qui ajoutera automatiquement
	l'utilisateur en amis si ils ne l'étaient pas déjà et qui affichera une pop-up
</p>
<q>
	<b>nom de l'organisateur</b> vous as invité à rejoindre <b>nom de la soirée</b>.
	<br />
	Voulez vous rejoindre cette soirée ?
</q>
<p>(Vous pourrez tout de même inviter des amis après création de la soirée.)</p>

<h2>Sponsoriser ma soirée</h2>
<p>
	L'utilisateur aura plusieurs choix de sponsorisation avec un slider qui augmente le nombre de
	personne qu'il va toucher (en fonction de sa ville, ses filtres et tous les autres réglages de sa
	soirée)
</p>
<p>
	Plus il augmente le slider pour faire grimper le monde touché, plus les prix gonfles (bien sûr, si
	les critères de sa soirée ne touche pas beaucoup de monde, son slider ne pourra pas aller très
	loin) (ex: Une soirée sans fumeurs et sans alcool dans un petit village de marne la vallé)
</p>
<p>
	Si l'utilisateur n'a pas lié de système de paiement à son compte, cela lui sera demandé. Dans la
	liste des sponsorisés, les soirées s'affichent en fonction du prix payé. Plus la sponsorisation
	est cher, plus la soirée est en pole position.
</p>

