	<li>Modifier son nom d'utilisateur</li>
	<li>Modifier sa photo de profil</li>
	<li>Modifier sa biographie</li>
	<li>Modifier sa date de naissance</li>
	<li>
		3 BOUTONS (GRID) -> "Demander une soirée" | "Créer une soirée" | "Voir mes soirées en cours"
	</li>
	<li>Ajouter / Modifier son moyen de paiement (facultatif)</li>
	<li>
		Demander une certification -> Une certification est un symbole "vérifié" qui permet d'indiquer
		que l'utilisateur est sérieux et qu'il n'y a rien à craindre -> Pour se faire certifier il faut
		avoir au moins participé à / créer une soirée et fournir sa carte d'identité à l'application
	</li>
	<li>
		Filtres -> L'utilisateur peut entrer une liste de chose qu'il recherche dans une soirée (Voir
		liste des filtres) | Il peut également entrer une moyenne d'age s'il le souhaite
	</li>
	<li>
		Liste d'amis -> Ajouter un ami à sa liste d'ami (recherche par nom d'utilisateur), Supprimer un
		ami
	</li>
	<li>Voir mon profil public</li>
	<li>
		Activer/Désactiver l'option "Ouvert à la demande" -> Cette option permet d'apparaitre dans la
		liste des créateurs de soirée pour les demandes de soirées. Pour activer cette option, il faut
		être obligatoirement certifié. -> Un bouton "Demander une soirée" s'affichera sur votre profil
		après l'activation de cette option (cela permettra aux utilisateurs qui ont gardé cet
		utilisateur en ami après une bonne expérience de pouvoir lui demander une soirée encore une
		fois)
	</li>
	<li>Lier un système de paiement (Compte en banque, Carte bancaire, PayPal etc...) (optionnel)</li>
	<li>
		Voir son porte-monnaie -> Montre le nombre d'argent qui a été récolté grâce à l'organisation
		d'une soirée après demande et/ou grâce à la fonctionnalité pourboire d'une soirée (Voir
		catégorie <a href="/Soirées/Créer sa Soirée">"CRÉER UNE SOIRÉE"</a>) L'argent dans le
		porte-monnaie peut être transféré à tout moment à leur compte en banque (RIB) / carte bancaire
		ou PayPal. Il n'y a pas de plafond pour se verser de l'argent (inspiré de Pumpkin)
	</li>
	<li>Supprimer son compte</li>
</ul>
