<p>Pop-up "Voulez vous rejoindre cette soirée ?" -> Allons-y ! | Non</p>

<p>
	Après avoir rejoins une soirée, l'organisateur de la soirée va recevoir une notification qui lui
	demande s'il accepte ou non notre demande. Il pourra inspecter le profil de l'utilisateur, voir
	comment il est noté, voir sa participation à des soirées et/ou sa création de soirée, voir les
	avis et commentaires qu'il a laissé, voir sa localisation et tous les autres éléments qui
	constitut l'affichage public d'un profil
</p>
<p>
	Si la demande est accepté, l'utilisateur est ajouté à un groupe de tchat qui constitue celui de la
	soirée.
</p>

<p>
	-> Sur la carte de la soirée, un bouton "inviter un/des amis" s'affiche. L'utilisateur pourra
	inviter ses amis à la soirée où il a été accepté. C'est à lui de se débrouiller avec
	l'organisateur pour lui dire d'accepter leur demandes.
</p>
<p>
	-> Sur la carte de la soirée, un bouton "quitter la soirée" vous permet de vous retirer de
	celle-ci. Vous restez amis avec ceux que vous avez déjà ajoutés mais la soirée ne s'affichera plus
	sur votre profil public
</p>

<h2>Groupe tchat</h2>
<p>
	Le message épinglé au dessus du tchat représente la description de la soirée. L'utilisateur peut
	écrire des messages et discuter avec les autres. L'utilisateur peut (si l'organisateur a activé la
	fonctionnalité) choisir parmis les mutltiples choix du sondage général "Qui ramène quoi ?" du
	groupe tchat (par exemple, l'utilisateur clique sur l'icone alcool, nourriture, déguisements,
	confettis etc...). Quand l'utilisateur clique sur un des choix, il peut y ajouter des précisions
	(par exemple, si l'utilisateur choisi nourriture, il peut ensuite préciser "Chips Lays" via un
	input). En cliquant sur le message épinglé, l'utilisateur peut voir la description en plus grand
	suivi de la liste des membres de la soirée. En cliquant sur un membre, que ce soit via la liste
	des membres ou sur leur photo quand ils écrivent sur le tchat, l'utilisateur peut avoir accès à
	leur profil public. Ils peuvent également cliquer sur le bouton "demander en ami" (voir <a
		href="/Utilisateurs/Profil">SYSTEME D'AMITIÉ</a
	>). Le groupe tchat peut contenir -> des messages vocaux, des envois de photos (pellicule ou prise
	direct par la caméra), des messages écrits.
</p>

<p>
	-> Une fois la soirée terminée, l'application vous demande si vous voulez fournir un pourboire à
	l'organisateur (s'il a activé la fonctionnalité (VOIR LA PARTIE <a href="/Soirées/Créer sa Soirée"
		>CRÉER UNE SOIRÉE</a
	>)), ensuite, l'application demande si l'utilisateur veut noter la soirée et écrire un commentaire
	(commentaire optionnel), enfin, une pop-up s'affiche avec la liste des membres de la soirée avec
	un bouton "ajouter en ami" à côté de chaque membres et un bouton "fermer" si l'utilisateur veut se
	débarasser de la popup.
</p>
