<h2>Barre de recherche (composant)</h2>
<p>Rechercher une soirée par nom | localisation</p>
<p>
	Filtre à côté de la barre de recherche (la liste du filtre sont
	<a href="/doc/Générique/Les Filtres">les filtres</a> du site)
</p>

<h2>Partiiz recommandées</h2>
<p>
	Soirées qui conviennent le plus à ce que l'utilisateur a entré dans ses informations (Zone Ville,
	Alentours et Région) (Rangées chronologiquements, de celle qui sont le plus à même de se passer à
	celle qui se passent dans plusieurs semaines / mois)
</p>

<h2>Partiiz proches</h2>
<p>
	Soirées les plus proche de la localisation de l'utilisateur (Zone Ville et Alentours) (Rangées
	chronologiquements, de celle qui sont le plus à même de se passer à celle qui se passent dans
	plusieurs semaines / mois)
</p>

<h2>Partiiz sponsorisées</h2>
<p>
	Soirées dont l'organisateur a payé pour en faire la promotion (Zone Ville, Alentours, Région et
	National) (Pas de rangements chronologiques, rangement en fonction du prix qui a été payé.)
</p>
<p>
	Le top 3 des soirées sponsorisées sont entourés d'un contour
	<br />
	OR(1er),
	<br />
	ARGENT(2eme),
	<br />
	BRONZE(3eme),
	<br />
	les autres n'auront pas de contours
</p>

