<p>La page "demander une soirée" se situe tout à gauche de l'application.</p>

<p><b>TROIS CATEGORIES</b></p>

<p>- Demander à un membre en particulier :</p>
<li>
	-> Barre de recherche pour rechercher un membre certifié (entrer un nom d'utilisateur ou une
	ville)
</li>
<li>
	-> Liste de membres certifiés qui ont activé l'option "Ouvert à la demande" sur leur profil et
	rangés en fonction de leur localisation (du plus au moins proche)
</li>
<p>
	- Faire une demande globale (Les membres certifiés pourront consulter votre demande dans la
	catégorie "les demandes actuelles")
</p>
<p>
	- Les demandes actuelles (catégorie seulement visible par les membres certifiés qui ont activé
	l'option "ouvert à la demande" sur leur profil)
</p>
<li>
	> Barre de recherche pour rechercher la demande d'un utilisateur (entrer un nom d'utilisateur ou
	une ville)
</li>
-
<li>-> Liste des demandes de soirées rangés dans l'ordre des plus au moins proche.</li>
<li>-> Filtre "Tous, Rémunérés, Non Rémunérés)</li>
<q
	>L'utilisateur pourra selectionner une demande et cliquer sur le bouton "prendre en charge la
	soirée". Le créateur de la demande en sera informé par notification "Votre soirée à été prise en
	charge par <b>NOM DE L'UTILISATEUR</b>" Une tchatbox entre les deux utilisateurs se créée
	directement puisque vous êtes instantanément membre de la soirée ET créateur de la demande. Le
	premier message de la tchatbox sera un récapitulatif de la demande que le demandeur a réalisé (un
	message robot/système).</q
>
<p>
	- Bouton "Signaler" (VOIR LA PARTIE <a href="/doc/Générique/Critères de Signalements"
		>CRITERES DE SIGNALEMENTS</a
	>)
</p>

<p>Après la selection d'un membre dans la liste OU une demande globale :</p>

<li>-> Entrer le nom de la soirée</li>
<li>-> Entrer la description de la soirée</li>
<li>
	-> Entrer la zone voulue (Ville, village, région, national etc...) (L'organisateur se charge du
	lieu)
</li>
<li>-> Entrer la date de la soirée</li>
<li>-> Entrer les filtres de la soirée</li>
<li>-> Désigner le nombre de personne recherchées</li>
<li>-> Entrer une moyenne d'age (optionnel)</li>
<li>
	-> Option "rémunéré / non rémunéré" (L'utilisateur précise s'il pourra payer des frais à
	l'organisateur ou non. Les frais seront défini plus tard par l'organisateur en fonction de la
	demande (VOIR LA PARTIE <a href="/doc/Générique/Systemes de Paiements">SYSTEME DE PAIEMENTS</a>))
</li>
<li>
	-> Option "Demander à l'organisateur d'inviter du monde" (L'organisateur devra prendre en charge
	le fait de trouver des gens et de les inviter à la soirée. Il peut également selectionner ses
	amis, cependant l'utilisateur peut toujours inviter ses propres amis sans même qu'une demande
	d'invitation soit créé. Si la soirée est déjà pleine, le nombre de personne augmentera de 1 à
	chaque fois que l'utilisateur invite un ami. C'est à l'organisateur de retirer des membres si il y
	en a trop par rapport à la demande de base. Attention : l'organisateur ne peut pas retirer de la
	soirée les amis qui ont été ajouté par le demandeur).
</li>
<li>-> Bouton "Demander ma soirée !"</li>

<q
	>Si c'est une demande ciblée à un membre, il recevra une notification "vous avez une nouvelle
	demande de soirée". Il pourra alors l'accepter ou la décliner. L'acceptation ou le déclin de la
	demande enverra dans les deux cas une notification à l'utilisateur pour lui en informer.</q
>
