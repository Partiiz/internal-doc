<h2>Demande de soirées</h2>
<p>
	Si une demande de soirée rémunérée à été accepté alors une tchatbox est lancé entre l'organisateur
	et le demandeur, avec comme premier message un message robot/système qui est le récapitulatif de
	la demande. Via la tchatbox, l'organisateur peut faire une proposition de prix au demandeur en
	fonction de la demande.
</p>
<p>
	C'est ce qu'on appelle la "phase négociation" (qui sera par ailleurs marquée comme status de la
	tchatbox, en haut, dans le titre de celle-ci).
</p>
<p>
	L'utilisateur peut accepter la proposition de prix ou refuser. S'il accepte la phase de
	négociation est terminée et on passe à la phase "création de la soirée...".
</p>
<p>
	Si le demandeur refuse, il peut à son tour proposer un prix. Si l'organisateur accepte, on passe à
	la phase suivante (création de la soirée), sinon il a le choix entre refuser et proposer un autre
	prix ou alors annuler la demande. S'il propose un autre prix, comme à son habitude le demandeur
	peut accepter ou refuser, mais cette-fois ci il peut lui aussi annuler la demande.
</p>
<p>
	En bref, le bouton "annuler la demande" est présent à partir du moment où une négociation à été
	refusée. Le paiement s'effectue automatiquement après la soirée et l'argent est envoyé dans le
	porte-monnaie in-app de l'organisateur.
</p>

<p>
	Cependant, si l'utilisateur n'a pas été satisfait de ce qui lui a été organisé, il peut revenir
	sur la carte de la soirée et un bouton "signaler mon organisateur" y est présent. Il faudra y
	fournir des preuves (description des évènements, photos de la soirée).
</p>

<p>
	Le support Partiiz enquêtera sur le sujet (vérification si la localisation de l'organisateur était
	bien au même endroit que le demandeur, analyse des photos et vérifications de la véracitée des
	propos dans la description de la plainte) et remboursera le demandeur si le résultat est positif.
</p>

<p>
	L'organisateur perdra son statut certifié et sera bloqué de l'application durant 1 mois. En
	fonction de la plainte, si elle est plus ou moins grave, Partiiz pourra porter plainte contre
	l'organisateur en se servant de sa carte d'identité.
</p>

<p>Si le résultat est négatif, le demandeur ne sera pas remboursé.</p>
<p>
	(Nous espérons que ces cas de signalements d'organisateurs arrivent très rarement grâce au fait
	qu'ils ont certifiés et donc qu'ils ont déjà créé / participé à une soirée qui s'est bien passée +
	leur carte d'identité qui est fournie lors de la demande de certification).
</p>

<h2 id="pourboires">Pourboires</h2>
<p>
	Quand une soirée est terminée, les membres peuvent, s'ils le souhaitent, fournir un pourboire (si
	l'organisateur de la soirée a activé l'option). Ils peuvent rentrer le montant qu'ils veulent.
	S'ils n'ont pas lié de système de paiement à leur compte, une pop-up affichant un formulaire pour
	lier son système de paiement s'affichera. L'argent sera versé dans le porte-monnaie in-app de
	l'organisateur.
</p>

<h2>Sponsoriser sa soirée</h2>
<p>
	La sponsorisation de soirée est l'un des moyens qui permet à Partiiz d'être rémunéré. Quand un
	utilisateur sponsorise sa soirée (voir partie <a href="/doc/Soirées/Créer sa Soirée"
		>"CRÉER SA SOIRÉE"</a
	>
	et
	<a href="/doc/Soirées/Gérer sa soirée">"GÉRER SA SOIRÉE"</a>), l'argent est deversé à Partiiz
</p>

<h2>Précision</h2>
<h3>Système de paiement</h3>
<p>
	Nous n'avons actuellement pas assez d'expérience en terme de monétisation / système de paiement
	pour savoir comment l'argent doit être transféré ou quel service utiliser. Il n'y a donc pas
	encore de cahier des charges concernant une page de système de paiement.
</p>
<h3>Wallet</h3>
<p>
	De même, nous n'avons pas assez d'expérience en terme de monétisation / système de paiement pour
	savoir comment gérer un système de porte-monnaie.
</p>
